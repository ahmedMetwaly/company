    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(ename.getText().isEmpty() || eadd.getText().isEmpty() || eadd.getText().isEmpty() ||
           erank.getText().isEmpty() || ehr.getText().isEmpty() || eoverhr.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter All Data");
        }
         else{
            int Index = elist.getSelectedIndex();
            emps.get(Index).setName(ename.getText().trim());
        
            emps.get(Index).setRank(erank.getText().trim());
            
            emps.get(Index).setAddress(eadd.getText().trim());
            try{
                 emps.get(Index).setPayment(Integer.parseInt(ehr.getText().trim()), Integer.parseInt(eoverhr.getText().trim()));
                emps.get(Index).setHoursWorked(Integer.parseInt(ehr.getText().trim()));
                emps.get(Index).setOverTimeHours(Integer.parseInt(eoverhr.getText().trim()));
               
                savAfterUpgrade();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "It Must Be A Number");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

