    //to retrieve data
    public void getList(){ 
        try{
            FileInputStream file = new FileInputStream("Data.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);
            boolean f=false;
            while(!f){
                try{
                    emps.add((Employee)inputFile.readObject());
                }
                catch(EOFException e){
                    f = true;
                }
                catch(Exception F){
                    JOptionPane.showMessageDialog(null, F.getMessage());
                }
            }
            inputFile.close();
        }
        catch(IOException z){
            JOptionPane.showMessageDialog(null, z.getMessage());
        }
    }
    
    // to save after deleting
 public void saveAfterDeleteing(){
        try{
            FileOutputStream file = new FileOutputStream("Data.dat");
            ObjectOutputStream oFile = new ObjectOutputStream(file);
            for (int i = 0; i < emps.size(); i++) {
                oFile.writeObject(emps.get(i));
            }
            oFile.close();
            JOptionPane.showMessageDialog(null, "Deleted");   
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
 // tp save after editing
 public void savAfterUpgrade(){ 
        try{
            FileOutputStream file = new FileOutputStream("Data.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            for (int i = 0; i < emps.size(); i++) {
                outputFile.writeObject(emps.get(i));
            }
            outputFile.close();
            JOptionPane.showMessageDialog(null, "Successfully Saved!!!");
        }
        catch(IOException x){
            JOptionPane.showMessageDialog(null, x.getMessage());
        }
    }
